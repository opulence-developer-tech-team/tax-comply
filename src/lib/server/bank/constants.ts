/**
 * Get bank by code
 */
export function getBankByCode(code: string): BankInfo | undefined {
  return NIGERIAN_BANKS.find((bank) => bank.code === code);
}

/**
 * Get all banks (sorted alphabetically by name)
 */
export function getAllBanks(): BankInfo[] {
  return [...NIGERIAN_BANKS].sort((a, b) => a.name.localeCompare(b.name));
}
